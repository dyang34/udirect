					$('input[name=consult_name]').val("");
					$('input[name=consult_hp]').val("");
					$('textarea[name=consult_content]').val("");
					$('#chk_agree').prop('checked', false);

					alert("상담예약이 완료되었습니다.    ");
				} else {
					alert(data.msg);
				}

				mc_consult_submitted = false;
				return false;
			},
			error : function(err)
			{
				alert(err.responseText);
			
				mc_consult_submitted = false;
